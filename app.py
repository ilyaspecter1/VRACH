# --- БОКОВАЯ ПАНЕЛЬ: Статистика смены ---
with st.sidebar:
    st.image("https://flaticon.com", width=100)
    st.header("🏥 Смена врача")
    st.info(f"**Врач:** Терапевт дежурный\n\n**Дата:** {datetime.now().strftime('%d.%m.%Y')}")
    
    # Расчет статистики по текущей сессии
    df_stat = pd.DataFrame(st.session_state['db_patients'])
    if not df_stat.empty:
        total = len(df_stat)
        appeared = len(df_stat[df_stat['status'] == "Пришел"])
        missed = len(df_stat[df_stat['status'] == "Не пришел"])
        st.metric("Всего пациентов в логе", total)
        st.success(f"Принято: {appeared}")
        st.error(f"Не явились: {missed}")
    else:
        st.metric("Пациентов принято", 0)

# --- ГЛАВНЫЙ ИНТЕРФЕЙС ---
st.title("💼 Электронное рабочее место врача-терапевта (МИС 24/7)")

tab1, tab2 = st.tabs(["🩺 Текущий прием и назначения", "🗄️ Электронный журнал приемов"])

with tab1:
    col_left, col_right = st.columns([1, 1.2])
    
    with col_left:
        st.subheader("📝 Шаг 1: Идентификация и статус")
        
        # Защита персональных данных: использование ID карты
        patient_id = st.text_input("Номер амбулаторной карты (или ID):", "АК-")
        
        status_visit = st.radio(
            "Статус явки на прием:",
            ["🟢 Пациент явился на прием", "🔴 Пациент НЕ явился на прием"],
            help="Зафиксируйте факт явки перед началом осмотра"
        )
        
        st.markdown("---")
        st.subheader("🩺 Шаг 2: Клиническая картина")
        
        diag_chosen = st.selectbox("Установите диагноз (МКБ-10):", DIAGNOSES)
        
        anamnesis = st.text_area(
            "Жалобы и анамнез (кратко):", 
            placeholder="Жалобы на сухой кашель, слабость, повышение температуры до 38.2... Анамнез: болеет 2-й день..."
        )

    with col_right:
        st.subheader("💊 Шаг 3: Назначение стандартов лечения")
        st.caption("Отметьте галочками необходимые позиции. Справочник подгружен автоматически.")
        
        st.write("**Рекомендуемые лекарственные препараты:**")
        selected_drugs = []
        for drug in DRUGS.get(diag_chosen, []):
            if st.checkbox(drug, key=f"d_{drug}"):
                selected_drugs.append(drug)
                
        st.write("**Рекомендуемый план обследования:**")
        selected_exams = []
        for exam in EXAMS.get(diag_chosen, []):
            if st.checkbox(exam, key=f"e_{exam}"):
                selected_exams.append(exam)
        
        st.markdown("---")
        st.subheader("🖨️ Шаг 4: Электронный протокол лечения")
        
        # Формирование текста протокола
        is_appeared = "Пришел" if "явился" in status_visit else "Не пришел"
        time_now = datetime.now().strftime("%H:%M:%S")
        
        protocol_text = f"ПРОТОКОЛ ПРИЕМА ПАЦИЕНТА [{patient_id}]\n"
        protocol_text += f"Дата/Время: {datetime.now().strftime('%d.%m.%Y')} в {time_now}\n"
        protocol_text += f"Статус визита: {is_appeared.upper()}\n"
        protocol_text += "----------------------------------------\n"
        
        if is_appeared == "Пришел":
            protocol_text += f"ДИАГНОЗ ПО МКБ: {diag_chosen}\n\n"
            protocol_text += f"АНАМНЕЗ И ЖАЛОБЫ:\n{anamnesis if anamnesis else 'Не указаны'}\n\n"
            
            protocol_text += "НАЗНАЧЕННОЕ ЛЕЧЕНИЕ:\n"
            if selected_drugs:
                for sd in selected_drugs:
                    protocol_text += f"- {sd}\n"
            else:
                protocol_text += "- Медикаментозное лечение не назначено.\n"
                
            protocol_text += "\nРЕКОМЕНДОВАННОЕ ОБСЛЕДОВАНИЕ:\n"
            if selected_exams:
                for se in selected_exams:
                    protocol_text += f"- {se}\n"
            else:
                protocol_text += "- Дополнительные обследования не требуются.\n"
        else:
            protocol_text += "Пациент не явился на запланированный прием. Прием аннулирован.\n"
            
        protocol_text += "----------------------------------------\n"
        protocol_text += "Врач: Дежурный терапевт"
        
        # Вывод предпросмотра
        st.code(protocol_text, language="text")
        
        # Кнопка фиксации в базу данных сайта
        if st.button("💾 Сохранить прием и занести в журнал"):
            st.session_state['db_patients'].append({
                "Карта": patient_id,
                "Время": time_now,
                "status": is_appeared,
                "Диагноз": diag_chosen if is_appeared == "Пришел" else "НЕТ ЯВКИ",
                "Лечение (кол-во)": len(selected_drugs),
                "Обследования (кол-во)": len(selected_exams)
            })
            st.success(f"Запись по пациенту {patient_id} успешно зафиксирована в системе!")
            st.download_button(
                label="📥 Скачать протокол для печати (TXT)",
                data=protocol_text,
                file_name=f"protocol_{patient_id}_{datetime.now().strftime('%Y%m%d')}.txt",
                mime="text/plain"
            )

with tab2:
    st.subheader("📊 Лог всех посещений за текущие сутки")
    if st.session_state['db_patients']:
        # Отображение данных в виде красивой таблицы
        df_view = pd.DataFrame(st.session_state['db_patients'])
        
        # Переименуем статус для наглядности в таблице
        df_view['Статус'] = df_view['status'].apply(lambda x: "🟢 Принят" if x == "Пришел" else "🔴 Пропуск")
        
        # Выводим только нужные колонки
        st.dataframe(
            df_view[["Карта", "Время", "Статус", "Диагноз", "Лечение (кол-во)", "Обследования (кол-во)"]],
            use_container_width=True
        )
        
        if st.button("🗑️ Очистить журнал смены"):
            st.session_state['db_patients'] = []
            st.rerun()
    else:
        st.info("Журнал пуст. Зафиксируйте первый прием во вкладке сверху.")
